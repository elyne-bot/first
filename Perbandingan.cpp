#include <iostream>
int main() {
 int x = 20, y = 100;
 std::cout << "x == y : " << (x == y) << std::endl;
 std::cout << "x != y : " << (x != y) << std::endl;
 std::cout << "x > y : " << (x > y) << std::endl;
 std::cout << "x < y : " << (x < y) << std::endl;
 std::cout << "x >= y : " << (x >= y) << std::endl;
 std::cout << "x <= y : " << (x <= y) << std::endl;
 return 0;
}
